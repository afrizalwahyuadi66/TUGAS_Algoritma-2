  # INPUT  : Angka ‘masukan 3 buah bilangan’
  # PROSES : Memasukan 3 bilangan dan mengalikannya
  # OUTPUT : Menampilkan perkalian dari 3 bilangan yang kita input
# -------------------------------------------------------------------------

# ALGORITMA----------------------------------------------------------------

# Menerima input dari pengguna
bilangan1 = float(input("Masukkan bilangan pertama: "))
bilangan2 = float(input("Masukkan bilangan kedua: "))
bilangan3 = float(input("Masukkan bilangan ketiga: "))

# Melakukan perhitungan
hasil_penjumlahan = bilangan1 + bilangan2 + bilangan3
hasil_perkalian = bilangan1 * bilangan2 * bilangan3

# Menampilkan hasil perhitungan
print(f"Hasil Penjumlahan: {hasil_penjumlahan}")
print(f"Hasil Perkalian  : {hasil_perkalian}")

